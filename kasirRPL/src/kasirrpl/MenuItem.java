    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        try {
            item u = new item();
            int newTotal = u.total;
            
            if (newTotal < 0) {
            JOptionPane.showMessageDialog(null, "Jumlah item tidak boleh kurang dari 0!");
            return;
            }
            this.statement = k.connection().prepareStatement("update item set nama_item = ?,harga = ?, status= ?, total= ? where item_id = ?");
            
            statement.setString(1, u.nama_item);
            statement.setInt(2, u.harga);
            statement.setString(3, u.status);
//            statement.setString(4, jTextFieldID.getText());
            statement.setInt(4, newTotal);
            statement.setString(5, jTextFieldID.getText());
            statement.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonUpdateActionPerformed

