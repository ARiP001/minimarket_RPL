    class item {
        int item_id, harga, total;
        String nama_item, status;
        
        public item (){
            this.nama_item = jTextFieldNama.getText();
            this.harga = Integer.parseInt(jTextFieldHarga.getText());
            this.status = jComboBoxReadiness.getSelectedItem().toString();
            this.total = Integer.parseInt(jTextFieldTotal.getText());
        }
    }
   
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Item");
        model.addColumn("Nama Item");
        model.addColumn("Harga");
        model.addColumn("Status Item");
        model.addColumn("Total");
        jTableMenu.setModel(model);
        try {
            this.statement = k.connection().prepareStatement("select * from item");
            this.rs = this.statement.executeQuery();
            while (rs.next()){
                Object[] data = {
                    rs.getString("item_id"),
                    rs.getString("nama_item"),
                    rs.getString("harga"),
                    rs.getString("status"),    
                    rs.getString("total")
                };
                model.addRow(data);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        jTextFieldID.setText("");
        jTextFieldNama.setText("");
        jTextFieldHarga.setText("");
        jTextFieldTotal.setText("");
    }
